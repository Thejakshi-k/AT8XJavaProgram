package sep.ex_13092024;

public class task2_cal {
    public static void main(String[] args) {
        double a = 34;
        double b = 10;
        System.out.println("Simple calculator");
        System.out.printf("%.2f + %.2f = %.2f \n",a,b,a+b);
        System.out.printf("%.2f - %.2f = %.2f \n",a,b,a-b);
        System.out.printf("%.2f * %.2f = %.2f \n",a,b,a*b);
        System.out.printf("%.2f / %.2f = %.2f \n",a,b,a/b);
        System.out.printf("%.2f %% %.2f = %.2f \n",a,b,a%b);


    }
}
