package sep.task_27_sep;

public class task_1 {
    public static void main(String[] args) {
        //while  and do-while loop
        int i = 1;
        int j = 1;
        while (i <= 10) {
            System.out.println("Pring 1 to 10 numbers for i" + i);
            i++;

        }
        do {
            System.out.println("Pring 1 to 10 numbers for j" + j);
            j++;
        } while (j <= 10);
    }
}
